print(thisset)

thisset = {"apple", "banana", "cherry", "apple"} # double value is ignored

print(thisset)

thisset = {"apple", "banana", "cherry", True, 1, 2} # True and 1 is same

print(thisset)

thisset = {"apple", "banana", "cherry", False, 0, 1} # false and 0 is same

print(thisset)
